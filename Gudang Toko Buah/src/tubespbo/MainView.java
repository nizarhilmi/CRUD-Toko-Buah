    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed

        String KodeBuah=jTextKodeBuah.getText();
        String NamaBuah=jTextNamaBuah.getText();
        String Rak=jComboBoxRak.getSelectedItem().toString();
        String Harga=jTextHarga.getText();
        String Jumlah=jTextJumlah.getText();

        java.util.Date tanggal = new java.util.Date();
        java.text.SimpleDateFormat TanggalFormat= new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String Waktu=TanggalFormat.format(tanggal);

        if (KodeBuah.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Kode buah tidak boleh kosong");
            jTextKodeBuah.requestFocus();
        }else if (NamaBuah.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Nama buah tidak boleh kosong");
            jTextNamaBuah.requestFocus();
        }else if (Rak.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Lokasi Rak buah tidak boleh kosong");
            jComboBoxRak.requestFocus();
        }else if (Harga.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Harga buah tidak boleh kosong");
            jTextHarga.requestFocus();
        }else if (Jumlah.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Jumlah buah tidak boleh kosong");
            jTextJumlah.requestFocus();
        }
        else if (!(new Scanner(Harga).hasNextInt())) {
            JOptionPane.showMessageDialog(null,"Harga produk harus angka");
            jTextHarga.requestFocus();
        }else if (!(new Scanner(Jumlah).hasNextInt())) {
            JOptionPane.showMessageDialog(null,"Stok produk harus angka");
            jTextJumlah.requestFocus();
        }else{
            try{
                Connection conn=(Connection)koneksi.getKoneksi();
                Statement stt=conn.createStatement();
                stt.executeUpdate("insert into produk(KodeBuah,NamaBuah,Harga,Jumlah,Rak,Waktu)"+
                    "VALUES('"+KodeBuah+"','"+NamaBuah+"','"+Harga+"','"+Jumlah+"','"+Rak+"','"+Waktu+"')");
                removeData();
                showData();
                SetEditOff();
                JOptionPane.showMessageDialog(this,"Data berhasil disimpan","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Simpan data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

