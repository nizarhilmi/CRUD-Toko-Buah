    private void cariData(String kunci){
        try{
            Object[] judul_kolom = {"Kode Buah", "Nama Buah", "Harga/[kg]", "Jumlah[kg]", "Rak", "Waktu"};
            tabModel=new DefaultTableModel(null,judul_kolom);
            TabelProduk1.setModel(tabModel);
            
            Connection conn=(Connection)koneksi.getKoneksi();
            Statement stt=conn.createStatement();
            tabModel.getDataVector().removeAllElements();
            
            rsBuah=stt.executeQuery("SELECT * from produk WHERE KodeBuah LIKE '%"+kunci+"%' OR NamaBuah LIKE '%"+kunci+"%'");  
            while(rsBuah.next()){
                Object[] data={
                    rsBuah.getString("KodeBuah"),
                    rsBuah.getString("NamaBuah"),
                    rsBuah.getString("Harga"),
                    rsBuah.getString("Jumlah"),
                    rsBuah.getString("Rak"),
                    rsBuah.getString("Waktu")          
                };
               tabModel.addRow(data);
            }                
        } catch (SQLException ex) {
        System.err.println(ex.getMessage());
        }
    }
   
